function createSheet(columns: number, rows: number, clear: boolean) {
  const png = new PNG({ width: columns * size, height: rows * size })
  const fillColor = clear ? transparent : [5, 7, 10, 255] satisfies Rgba
  for (let y = 0; y < png.height; y++) {
    for (let x = 0; x < png.width; x++) set(png, x, y, fillColor)
  }
  return png
}

function drawTile(sheet: PNG, tileX: number, tileY: number, draw: (tile: PNG) => void) {
  const tile = createSheet(1, 1, true)
  draw(tile)
  blit(tile, sheet, tileX * size, tileY * size)
}

function savePng(png: PNG, filename: string) {
  writeFileSync(join(outDir, filename), PNG.sync.write(upscaleNearest(png, upscaleFactor)))
}

function upscaleNearest(src: PNG, factor: number) {
  if (factor === 1) return src

  const dst = new PNG({ width: src.width * factor, height: src.height * factor })
  for (let y = 0; y < src.height; y++) {
    for (let x = 0; x < src.width; x++) {
      const index = (src.width * y + x) << 2
      const color: Rgba = [src.data[index], src.data[index + 1], src.data[index + 2], src.data[index + 3]]
      for (let yy = 0; yy < factor; yy++) {
        for (let xx = 0; xx < factor; xx++) set(dst, x * factor + xx, y * factor + yy, color)
      }
    }
  }
  return dst
}

function blit(src: PNG, dst: PNG, dx: number, dy: number) {
  for (let y = 0; y < src.height; y++) {
    for (let x = 0; x < src.width; x++) {
      const index = (src.width * y + x) << 2
      set(dst, dx + x, dy + y, [src.data[index], src.data[index + 1], src.data[index + 2], src.data[index + 3]])
    }
  }
}

function set(png: PNG, x: number, y: number, color: Rgba | Rgb) {
  if (x < 0 || y < 0 || x >= png.width || y >= png.height) return
  const index = (png.width * Math.round(y) + Math.round(x)) << 2
  png.data[index] = color[0]
  png.data[index + 1] = color[1]
  png.data[index + 2] = color[2]
  png.data[index + 3] = color[3] ?? 255
}

function fill(png: PNG, x: number, y: number, width: number, height: number, color: Rgba) {
  for (let yy = y; yy < y + height; yy++) {
    for (let xx = x; xx < x + width; xx++) set(png, xx, yy, color)
  }
}

function rect(png: PNG, x: number, y: number, width: number, height: number, color: Rgb, filled: boolean) {
  if (filled) {
    fill(png, x, y, width, height, [...color, 255])
    return
  }
  line(png, x, y, x + width - 1, y, color)
  line(png, x, y + height - 1, x + width - 1, y + height - 1, color)
  line(png, x, y, x, y + height - 1, color)
  line(png, x + width - 1, y, x + width - 1, y + height - 1, color)
}

function line(png: PNG, x0: number, y0: number, x1: number, y1: number, color: Rgb) {
  let x = Math.round(x0)
  let y = Math.round(y0)
  const endX = Math.round(x1)
  const endY = Math.round(y1)
  const dx = Math.abs(endX - x)
  const sx = x < endX ? 1 : -1
  const dy = -Math.abs(endY - y)
  const sy = y < endY ? 1 : -1
  let error = dx + dy

  while (true) {
    set(png, x, y, [...color, 255])
    if (x === endX && y === endY) break
    const doubled = 2 * error
    if (doubled >= dy) {
      error += dy
      x += sx
    }
    if (doubled <= dx) {
      error += dx
      y += sy
    }
  }
}

function ellipse(png: PNG, x0: number, y0: number, x1: number, y1: number, color: Rgb, filled: boolean) {
  const cx = (x0 + x1) / 2
  const cy = (y0 + y1) / 2
  const rx = Math.max(1, (x1 - x0) / 2)
  const ry = Math.max(1, (y1 - y0) / 2)
  for (let y = Math.floor(y0); y <= Math.ceil(y1); y++) {
    for (let x = Math.floor(x0); x <= Math.ceil(x1); x++) {
      const d = ((x - cx) * (x - cx)) / (rx * rx) + ((y - cy) * (y - cy)) / (ry * ry)
      if (filled ? d <= 1 : d > 0.74 && d <= 1.16) set(png, x, y, [...color, 255])
    }
  }
}

function diamond(png: PNG, cx: number, cy: number, radius: number, color: Rgb) {
  for (let y = cy - radius; y <= cy + radius; y++) {
    const half = radius - Math.abs(y - cy)
    line(png, cx - half, y, cx + half, y, color)
  }
  line(png, cx, cy - radius, cx + radius, cy, darken(color, 0.45))
  line(png, cx + radius, cy, cx, cy + radius, darken(color, 0.45))
  line(png, cx, cy + radius, cx - radius, cy, darken(color, 0.45))
  line(png, cx - radius, cy, cx, cy - radius, darken(color, 0.45))
}

function triangle(png: PNG, ax: number, ay: number, bx: number, by: number, cx: number, cy: number, color: Rgb) {
  const minX = Math.floor(Math.min(ax, bx, cx))
  const maxX = Math.ceil(Math.max(ax, bx, cx))
  const minY = Math.floor(Math.min(ay, by, cy))
  const maxY = Math.ceil(Math.max(ay, by, cy))
  const area = edge(ax, ay, bx, by, cx, cy)
  for (let y = minY; y <= maxY; y++) {
    for (let x = minX; x <= maxX; x++) {
      const w0 = edge(bx, by, cx, cy, x, y)
      const w1 = edge(cx, cy, ax, ay, x, y)
      const w2 = edge(ax, ay, bx, by, x, y)
      if ((area >= 0 && w0 >= 0 && w1 >= 0 && w2 >= 0) || (area < 0 && w0 <= 0 && w1 <= 0 && w2 <= 0)) set(png, x, y, [...color, 255])
    }
  }
}

function edge(ax: number, ay: number, bx: number, by: number, px: number, py: number) {
  return (px - ax) * (by - ay) - (py - ay) * (bx - ax)
}

function arc(png: PNG, x0: number, y0: number, x1: number, y1: number, color: Rgb) {
  const cx = (x0 + x1) / 2
  const cy = (y0 + y1) / 2
  const rx = Math.abs(x1 - x0) / 2
  const ry = Math.abs(y1 - y0) / 2
  for (let a = -80; a <= 80; a += 6) {
    const radians = (a * Math.PI) / 180
    set(png, Math.round(cx + Math.cos(radians) * rx), Math.round(cy + Math.sin(radians) * ry), [...color, 255])
  }
}

function drawNumber(png: PNG, value: number, cx: number, cy: number, color: Rgb) {
  const text = String(value)
  const start = cx - text.length * 3
  for (let index = 0; index < text.length; index++) drawDigit(png, Number(text[index]), start + index * 6, cy - 4, color)
}

function drawDigit(png: PNG, digit: number, x: number, y: number, color: Rgb) {
  const segments: Record<number, string> = {
    0: "abcfed",
    1: "bc",
    2: "abged",
    3: "abgcd",
    4: "fgbc",
    5: "afgcd",
    6: "afgecd",
    7: "abc",
    8: "abcdefg",
    9: "abfgcd",
  }
  const draw = segments[digit] ?? ""
  if (draw.includes("a")) line(png, x, y, x + 4, y, color)
  if (draw.includes("b")) line(png, x + 4, y, x + 4, y + 4, color)
  if (draw.includes("c")) line(png, x + 4, y + 4, x + 4, y + 8, color)
  if (draw.includes("d")) line(png, x, y + 8, x + 4, y + 8, color)
  if (draw.includes("e")) line(png, x, y + 4, x, y + 8, color)
  if (draw.includes("f")) line(png, x, y, x, y + 4, color)
  if (draw.includes("g")) line(png, x, y + 4, x + 4, y + 4, color)
}

function scatter(png: PNG, seed: number, light: Rgb, dark: Rgb) {
  for (let i = 0; i < 28; i++) {
    const x = (seed * 17 + i * 11) % size
    const y = (seed * 23 + i * 7) % size
    set(png, x, y, [...(i % 2 ? light : dark), 255])
  }
}

function lighten(color: Rgb, factor: number): Rgb {
  return color.map((component) => clamp(Math.round(component * factor), 0, 255)) as Rgb
}

function darken(color: Rgb, factor: number): Rgb {
  return lighten(color, factor)
}

function clamp(value: number, min: number, max: number) {
  return Math.max(min, Math.min(max, value))
}
