function writeTerrainSheet() {
  const sheet = createSheet(8, 1, false)
  drawTile(sheet, 0, 0, (tile) => floor(tile, 0))
  drawTile(sheet, 1, 0, (tile) => floor(tile, 1))
  drawTile(sheet, 2, 0, (tile) => floor(tile, 2))
  drawTile(sheet, 3, 0, (tile) => wall(tile, 0))
  drawTile(sheet, 4, 0, (tile) => wall(tile, 1))
  drawTile(sheet, 5, 0, stairs)
  drawTile(sheet, 6, 0, door)
  drawTile(sheet, 7, 0, voidTile)
  savePng(sheet, "terrain.png")
}

function writeItemSheet() {
  const sheet = createSheet(8, 3, true)
  const order = Object.entries(staticSpriteCrops)
    .filter(([, crop]) => crop.sheet === "items")
    .sort((a, b) => a[1].tileY - b[1].tileY || a[1].tileX - b[1].tileX)

  for (const [id, crop] of order) {
    drawTile(sheet, crop.tileX, crop.tileY, (tile) => item(tile, id))
  }

  savePng(sheet, "items.png")
}

function writeActorSheet() {
  const rows = animatedSpriteIds.length * spriteAnimations.length
  const sheet = createSheet(4, rows, true)

  animatedSpriteIds.forEach((id, actorIndex) => {
    spriteAnimations.forEach((animation, animationIndex) => {
      for (let frame = 0; frame < 4; frame++) {
        drawTile(sheet, frame, actorIndex * spriteAnimations.length + animationIndex, (tile) => actor(tile, id, animation, frame))
      }
    })
  })

  savePng(sheet, "actors.png")
}

function writeD20Sheet() {
  const sheet = createSheet(12, 20, true)
  for (let result = 1; result <= 20; result++) {
    for (let frame = 0; frame < 12; frame++) drawTile(sheet, frame, result - 1, (tile) => drawD20Frame(tile, result, frame))
  }
  savePng(sheet, "d20.png")
}

function floor(tile: PNG, variant: number) {
  const base = variant === 0 ? [42, 55, 61] : variant === 1 ? [48, 63, 68] : [39, 51, 58]
  fill(tile, 0, 0, size, size, [...base, 255])
  for (let y = 0; y < size; y += 8) line(tile, 0, y, size - 1, y, darken(base, 0.72))
  for (let x = variant * 3; x < size; x += 11) line(tile, x, 0, x, size - 1, darken(base, 0.82))
  scatter(tile, variant + 5, lighten(base, 1.18), darken(base, 0.7))
  if (variant === 2) {
    line(tile, 6, 22, 14, 17, darken(base, 0.55))
    line(tile, 15, 17, 25, 20, darken(base, 0.55))
  }
}

function wall(tile: PNG, variant: number) {
  const base = variant === 0 ? [79, 74, 86] : [69, 64, 76]
  fill(tile, 0, 0, size, size, [...base, 255])
  for (let row = 0; row < 4; row++) {
    const y = row * 8
    line(tile, 0, y, size - 1, y, darken(base, 0.56))
    for (let x = row % 2 ? 8 : 0; x < size; x += 16) line(tile, x, y, x, y + 7, darken(base, 0.6))
    fill(tile, 2 + ((row + variant) % 2) * 10, y + 2, 8, 4, [...lighten(base, 1.14), 255])
  }
  rect(tile, 1, 1, size - 2, size - 2, darken(base, 0.48), false)
  scatter(tile, variant + 13, lighten(base, 1.08), darken(base, 0.62))
}

