function drawSourceFrame(tile: PNG, src: PNG, sx: number, sy: number, sw: number, sh: number) {
  const bounds = sourceBounds(src, sx, sy, sw, sh)
  if (!bounds) return

  const scale = Math.min((size - 6) / bounds.width, (size - 4) / bounds.height)
  const width = Math.max(1, Math.floor(bounds.width * scale))
  const height = Math.max(1, Math.floor(bounds.height * scale))
  const ox = Math.floor((size - width) / 2)
  const oy = Math.floor(size - height)

  for (let y = 0; y < height; y++) {
    for (let x = 0; x < width; x++) {
      const sourceX = bounds.x + Math.min(bounds.width - 1, Math.floor(x / scale))
      const sourceY = bounds.y + Math.min(bounds.height - 1, Math.floor(y / scale))
      const sourceIndex = (src.width * sourceY + sourceX) << 2
      const alpha = src.data[sourceIndex + 3]
      if (alpha < 24) continue
      set(tile, ox + x, oy + y, [src.data[sourceIndex], src.data[sourceIndex + 1], src.data[sourceIndex + 2], alpha])
    }
  }
}

function drawMaskedSourceFrame(
  tile: PNG,
  src: PNG,
  sx: number,
  sy: number,
  sw: number,
  sh: number,
  predicate: (src: PNG, x: number, y: number) => boolean,
  tint?: (r: number, g: number, b: number) => Rgb,
) {
  const bounds = sourceBounds(src, sx + 96, sy + 96, sw - 192, sh - 192, predicate)
  if (!bounds) return

  const scale = Math.min((size - 4) / bounds.width, (size - 4) / bounds.height)
  const width = Math.max(1, Math.floor(bounds.width * scale))
  const height = Math.max(1, Math.floor(bounds.height * scale))
  const ox = Math.floor((size - width) / 2)
  const oy = Math.floor((size - height) / 2)

  for (let y = 0; y < height; y++) {
    for (let x = 0; x < width; x++) {
      const sourceX = bounds.x + Math.min(bounds.width - 1, Math.floor(x / scale))
      const sourceY = bounds.y + Math.min(bounds.height - 1, Math.floor(y / scale))
      if (!predicate(src, sourceX, sourceY)) continue
      const sourceIndex = (src.width * sourceY + sourceX) << 2
      const color = tint?.(src.data[sourceIndex], src.data[sourceIndex + 1], src.data[sourceIndex + 2]) ?? [
        src.data[sourceIndex],
        src.data[sourceIndex + 1],
        src.data[sourceIndex + 2],
      ]
      set(tile, ox + x, oy + y, [...color, src.data[sourceIndex + 3]])
    }
  }
}

function isD20RollPixel(src: PNG, x: number, y: number) {
  const index = (src.width * y + x) << 2
  const r = src.data[index]
  const g = src.data[index + 1]
  const b = src.data[index + 2]
  const a = src.data[index + 3]
  if (a < 24) return false

  const redPanel = r > 24 && r > g * 1.8 && r > b * 1.8
  const greyFrame = Math.abs(r - g) < 12 && Math.abs(g - b) < 12 && r > 20 && r < 88
  return !redPanel && !greyFrame
}

function sourceBounds(src: PNG, sx: number, sy: number, sw: number, sh: number, predicate?: (src: PNG, x: number, y: number) => boolean) {
  let minX = sx + sw
  let minY = sy + sh
  let maxX = sx
  let maxY = sy

  for (let y = sy; y < sy + sh; y++) {
    for (let x = sx; x < sx + sw; x++) {
      if (x < 0 || y < 0 || x >= src.width || y >= src.height) continue
      if (predicate) {
        if (!predicate(src, x, y)) continue
      } else {
        const alpha = src.data[((src.width * y + x) << 2) + 3]
        if (alpha < 24) continue
      }
      minX = Math.min(minX, x)
      minY = Math.min(minY, y)
      maxX = Math.max(maxX, x)
      maxY = Math.max(maxY, y)
    }
  }

  if (maxX < minX || maxY < minY) return null
  minX = Math.max(sx, minX - 2)
  minY = Math.max(sy, minY - 2)
  maxX = Math.min(sx + sw - 1, maxX + 2)
  maxY = Math.min(sy + sh - 1, maxY + 2)
  return { x: minX, y: minY, width: maxX - minX + 1, height: maxY - minY + 1 }
}

function humanoid(tile: PNG, id: AnimatedSpriteId, p: ActorPalette, animation: SpriteAnimationId, frame: number) {
  const boss = id.startsWith("boss")
  const ox = animation === "walk" ? [-1, 1, 0, -1][frame] : animation === "attack-melee" ? [0, 1, 2, 0][frame] : 0
  const bob = animation === "idle" || animation === "talk" ? [0, 1, 0, 1][frame] : animation === "hurt" ? 1 : 0
  const cx = 16 + ox
  const headY = boss ? 7 + bob : 9 + bob
  const bodyY = boss ? 14 + bob : 16 + bob
  const scale = boss ? 1 : 0

  ellipse(tile, cx - 5 - scale, headY - 5, cx + 5 + scale, headY + 5, p.dark, true)
  ellipse(tile, cx - 4, headY - 4, cx + 4, headY + 4, p.skin, true)
  fill(tile, cx - 2, headY - 1, 2, 1, [...outline, 255])
  fill(tile, cx + 2, headY - 1, 2, 1, [...outline, 255])
  if (animation === "talk") fill(tile, cx - 2, headY + 3, 4, 1, [...p.accent, 255])
  if (animation === "shocked") {
    fill(tile, cx - 1, headY + 2, 2, 3, [...outline, 255])
    line(tile, cx, headY - 8, cx, headY - 12, p.glow)
  }

  if (id === "boss-minotaur") {
    line(tile, cx - 5, headY - 5, cx - 10, headY - 8, p.accent)
    line(tile, cx + 5, headY - 5, cx + 10, headY - 8, p.accent)
  }

  fill(tile, cx - 7 - scale, bodyY - 2, 14 + scale * 2, 10, [...p.cloth, 255])
  rect(tile, cx - 7 - scale, bodyY - 2, 14 + scale * 2, 10, outline, false)
  fill(tile, cx - 5, bodyY, 10, 3, [...p.armor, 255])
  fill(tile, cx - 2, bodyY - 1, 4, 11, [...p.accent, 255])

  const leftLeg = animation === "walk" ? [-2, 1, 2, -1][frame] : 0
  const rightLeg = -leftLeg
  line(tile, cx - 4, bodyY + 8, cx - 5 + leftLeg, 29, p.dark)
  line(tile, cx + 4, bodyY + 8, cx + 5 + rightLeg, 29, p.dark)

  const melee = animation === "attack-melee"
  const ranged = animation === "attack-ranged"
  const cast = animation === "cast"
  const leftHand = melee ? [cx - 8, bodyY + 2, cx - 13, bodyY - 1] : [cx - 7, bodyY, cx - 11, bodyY + 7]
  const rightHand = melee ? [cx + 7, bodyY, cx + 14, bodyY - 5] : [cx + 7, bodyY, cx + 11, bodyY + 7]
  line(tile, leftHand[0], leftHand[1], leftHand[2], leftHand[3], p.skin)
  line(tile, rightHand[0], rightHand[1], rightHand[2], rightHand[3], p.skin)

  if (id.includes("arcanist") || id.includes("oracle") || id.includes("lich") || id === "necromancer") {
    line(tile, cx + 10, bodyY + 8, cx + 14, bodyY - 9, p.weapon)
    diamond(tile, cx + 14, bodyY - 10, cast ? 5 + (frame % 2) : 3, p.glow)
  } else if (ranged || id.includes("ranger")) {
    arc(tile, cx + 6, bodyY - 2, cx + 16, bodyY + 12, p.weapon)
    line(tile, cx + 7, bodyY - 1, cx + 15, bodyY + 11, [230, 220, 188])
  } else {
    sword(tile, cx + 8, bodyY + 8, cx + 16, melee ? bodyY - 8 : bodyY - 1)
  }

  if (animation === "cast") {
    diamond(tile, cx - 11, bodyY - 4, 3 + (frame % 2), p.glow)
    diamond(tile, cx + 11, bodyY - 7, 2 + (frame % 2), p.accent)
  }
  if (animation === "hurt") line(tile, cx - 8, bodyY - 5, cx + 8, bodyY + 7, [225, 64, 72])
  if (animation === "death") {
    fill(tile, 5, 24, 22, 5, [...p.dark, 230])
    line(tile, 7, 24, 25, 28, p.accent)
  }
}

