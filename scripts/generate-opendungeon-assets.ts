function actor(tile: PNG, id: AnimatedSpriteId, animation: SpriteAnimationId, frame: number) {
  if (drawItchActor(tile, id, animation, frame)) return
  if (id === "slime") {
    slime(tile, palettes[id], animation, frame)
    return
  }
  humanoid(tile, id, palettes[id], animation, frame)
}

type ItchActorSource = {
  path: string
  frameWidth: number
  frameHeight: number
  frameCount?: number
  crop?: { x: number; y: number; width: number; height: number }
}

function drawItchActor(tile: PNG, id: AnimatedSpriteId, animation: SpriteAnimationId, frame: number) {
  const source = itchActorSource(id, animation)
  if (!source || !existsSync(source.path)) return false

  const png = PNG.sync.read(readFileSync(source.path))
  const frameCount = source.frameCount ?? Math.max(1, Math.floor(png.width / source.frameWidth))
  const sourceFrame = Math.min(frameCount - 1, Math.floor((frame / animationFrameCount) * frameCount))
  const crop = source.crop ?? { x: 0, y: 0, width: source.frameWidth, height: source.frameHeight }
  drawSourceFrame(tile, png, sourceFrame * source.frameWidth + crop.x, crop.y, crop.width, crop.height)
  return true
}

function itchActorSource(id: AnimatedSpriteId, animation: SpriteAnimationId): ItchActorSource | null {
  if (id === "hero-ranger") return zerieSource("Soldier", animation)
  if (id === "hero-warden") return samuraiSource(animation)
  if (id === "slime") return forestMushroomSource(animation)
  if (id === "ghoul" || id === "necromancer") return zerieSource("Orc", animation)
  return null
}

